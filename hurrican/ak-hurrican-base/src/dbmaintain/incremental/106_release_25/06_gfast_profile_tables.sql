
-- create table for g.fast profile
create table T_GFAST_PROFILE (
    ID NUMBER(19) PRIMARY KEY NOT NULL,
    NAME VARCHAR2(500) NOT NULL,
    BAUGRUPPE_TYP_ID NUMBER(19),
    VERSION NUMBER(19) DEFAULT 0 NOT NULL
);

GRANT SELECT, INSERT, UPDATE ON T_GFAST_PROFILE TO R_HURRICAN_USER;
GRANT SELECT ON T_GFAST_PROFILE TO R_HURRICAN_READ_ONLY;

CREATE SEQUENCE S_T_GFAST_PROFILE_0 START WITH 1 MAXVALUE 9999999999999999999 MINVALUE 1 NOCYCLE CACHE 20 NOORDER;
GRANT SELECT ON S_T_GFAST_PROFILE_0 TO PUBLIC;



-- create table for g.fast values
create table T_GFAST_AUFTRAG_VALUES (
    ID NUMBER(19) PRIMARY KEY NOT NULL,
    AUFTRAG_ID NUMBER(19) NOT NULL,
    PARAMETER_NAME VARCHAR2(2000) NOT NULL,
    PARAMETER_VALUE VARCHAR2(2000),
    PARAMETER_TYP VARCHAR2(100),
    VALID_FROM DATE,
